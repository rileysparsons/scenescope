#import "UIColor+SceneScopeColor.h"

@implementation UIColor (SceneScopeColor)

+(UIColor *)sceneScopeRed{
    return [UIColor colorWithRed:0.906 green:0.486 blue:0.561 alpha:1.0];
    //e77b8f
}

+(UIColor *)sceneScopeBlue{
    return [UIColor colorWithRed:0.4 green:0.533 blue:0.867 alpha:1.0];
    //6687dd
}

+(UIColor *)sceneScopeGreen{
    return [UIColor colorWithRed:186.0/255.0f green:233.0/255.0f blue:131.0/255.0f alpha:1.0];
    //bae983
}

@end
